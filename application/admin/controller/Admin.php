            $bgAnimate = BgAnimate::getSelect( [ 'status' => 1 ], 'id asc', 'id,key' );
            $this->assign( 'bgAnimate', $bgAnimate );

            $this->assign( 'data', $data );
            return $this->fetch();
        }
    }

