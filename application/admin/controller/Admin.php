    public function edit()
    {
        $id = input( 'id' );
        if( Request::isAjax() ){

            $errMsg = $this->validateData( 'Admin', $_POST );
            if( $errMsg ) return $errMsg;

            $status = $this->model->save( $_POST, [ 'id' => $id ] );
            $this->endAction( $status, 'ä¿®æ”¹ç®¡ç†å‘˜' );

        }else{

