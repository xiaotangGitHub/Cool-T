    public function del()
    {
        //æ¥æ”¶å‚æ•°å¹¶éªŒè¯
        $id = input( 'id' );
        if( !$id || !Request::isAjax() ) abort( 404, 'é¡µé¢ä¸å­˜åœ¨' );

        //åˆ é™¤æ•°æ®
