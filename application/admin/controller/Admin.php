     * @return \think\response\Json
     * @throws \think\db\exception\DataNotFoundException
     * @throws \think\db\exception\ModelNotFoundException
     * @throws \think\exception\DbException
     */
    public function setRole()
    {
        if( !Request::isAjax() ) abort( 404, 'é¡µé¢ä¸å­˜åœ¨' );

        $type = input( 'type' );
        $id = input( 'id' );
        $where = [ 'user_id' => $id ];

        if( $type == 'get' ){
            //è·å–è§’è‰²åˆ—è¡¨
            $roleIds = AdminRole::whereColumn( $where, 'role_id' );
            $role = RoleAdmin::getSelect( null,  null, 'id,name' );
            $role = $role->toArray();

            foreach ( $role as $key => $val ){
                if( in_array( $val['id'], $roleIds ) ){
                    $role[$key]['checked'] = true;
                }else{
                    $role[$key]['checked'] = false;
                }
            }

            return json( $role );
        }elseif( $type == 'set' ){
            //è®¾ç½®è§’è‰²
            $data = input( 'data' );
            if( $data && count( $data ) > 0 ){
                Db::startTrans();
                try{
                    AdminRole::where( $where )->delete();
                    AdminRole::insertAll( $data );
                    Db::commit();
                    $status = true;
                }catch ( \Exception $e ){
                    Db::rollback();
                    $status = false;
                }
            }else{
                $status = AdminRole::where( $where )->delete();
            }

            $this->endAction( $status, 'ç®¡ç†å‘˜è§’è‰²è®¾ç½®' );
        }
    }
